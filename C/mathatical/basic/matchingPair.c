// Given a set of numbers from 1 to N, each number is exactly present twice so there are N pairs. In the worst-case
// scenario, how many numbers X should be picked and removed from the set until we find a matching pair?

// Input:
// N = 2
// Output:
// 3
// Explanation:
// When N=2 then there are 
// 2 pairs, let them be {1,2,1,2}
// and a matching pair will 
// be made in 3 draws.


long int find(int N){
    return N+1;
}

int main(){
    int n;
    find(n);
    return 0;
}